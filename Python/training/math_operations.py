def soma(number_a: int, number_b: int):
    return number_a + number_b


def resta(number_a: int, number_b: int):
    return number_a - number_b


def mult(number_a: int, number_b: int):
    return number_a * number_b


def div(number_a: int, number_b: int):
    return number_a / number_b


def mod(number_a: int, number_b: int):
    return number_a % number_b
